/**
 * add - Adds two numbers
 * @a: int 1
 * @b: int 2
 * Return: Returns result
**/
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: int 1
 * @b: int 2
 * Return: Returns result
**/
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiplies two numbers
 * @a: int 1
 * @b: int 2
 * Return: Returns result
**/
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divides two numbers
 * @a: int 1
 * @b: int 2
 * Return: Returns result
**/
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Modules two numbers
 * @a: int 1
 * @b: int 2
 * Return: Returns result
**/
int mod(int a, int b)
{
	return (a % b);
}
